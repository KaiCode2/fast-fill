/**
 * Generates typed ABI modules from the Foundry artifacts in ../out. Run after `forge build`
 * (wired as a predev/prebuild hook). The artifacts are the source of truth — the Order tuple is
 * too large to hand-transcribe safely, and `as const` gives viem full type inference.
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url)); // demo/scripts
const repoRoot = resolve(scriptDir, "..", ".."); // repository root
const outDir = resolve(repoRoot, "out");
const genDir = resolve(scriptDir, "..", "src", "lib", "abis", "generated");

const CONTRACTS = [
  { file: "CctpAdapter.sol", name: "CctpAdapter", exportName: "cctpAdapterAbi" },
  { file: "OftAdapter.sol", name: "OftAdapter", exportName: "oftAdapterAbi" },
  { file: "FastFillConfig.sol", name: "FastFillConfig", exportName: "fastFillConfigAbi" },
];

if (!existsSync(outDir)) {
  console.error(
    `\n[gen-abis] Foundry artifacts not found at ${outDir}.\n` +
      `  Run \`forge build\` in the repo root before building the demo.\n`,
  );
  process.exit(1);
}

mkdirSync(genDir, { recursive: true });

const indexLines: string[] = [];
for (const c of CONTRACTS) {
  const artifactPath = resolve(outDir, c.file, `${c.name}.json`);
  if (!existsSync(artifactPath)) {
    console.error(`[gen-abis] Missing ${artifactPath}. Run \`forge build\` in the repo root.`);
    process.exit(1);
  }
  const artifact = JSON.parse(readFileSync(artifactPath, "utf8")) as { abi: unknown[] };
  const ts =
    `// AUTO-GENERATED by scripts/gen-abis.ts from out/${c.file}/${c.name}.json — do not edit.\n` +
    `export const ${c.exportName} = ${JSON.stringify(artifact.abi, null, 2)} as const;\n`;
  writeFileSync(resolve(genDir, `${c.name}.ts`), ts);
  indexLines.push(`export { ${c.exportName} } from "./${c.name}";`);
  console.log(`[gen-abis] wrote ${c.name}.ts (${artifact.abi.length} ABI entries)`);
}

writeFileSync(resolve(genDir, "index.ts"), indexLines.join("\n") + "\n");
console.log("[gen-abis] done.");
